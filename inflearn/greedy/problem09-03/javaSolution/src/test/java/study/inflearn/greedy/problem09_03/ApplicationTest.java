	@MethodSource("problem09_03_params")
	void solutionTest(String inputStr, int expect) {
		Application app = new Application();
		String[] splitStr = inputStr.trim().split("\n");
		int n = Integer.parseInt(splitStr[0]);
		Guest[] guests = new Guest[n];
		for (int i = 0; i < n; i++) {
			int[] inputs = Arrays.stream(splitStr[i + 1].trim().split(" "))
				.mapToInt(Integer::parseInt)
				.toArray();
			guests[i] = new Guest(inputs[0], inputs[1]);
		}

		int answer = app.solution(guests);

		assertEquals(expect, answer);
	}

